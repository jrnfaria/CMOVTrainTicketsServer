DROP TABLE IF EXISTS USER;
DROP TABLE IF EXISTS TICKET;
DROP TABLE IF EXISTS CREDITCARD;
DROP TABLE IF EXISTS TIMETABLESTATION;
DROP TABLE IF EXISTS TIMETABLE;
DROP TABLE IF EXISTS STATION;

CREATE TABLE USER (ID INTEGER PRIMARY KEY AUTOINCREMENT,NAME TEXT, USERNAME TEXT, PASSWORD TEXT);
CREATE TABLE TICKET(ID INTEGER PRIMARY KEY AUTOINCREMENT,TICKETID INTEGER,DEPARTURE TEXT, ARRIVAL TEXT, TRAIN TEXT, DEPARTUREDATE TEXT, USER REFERENCES USER(USERNAME));
CREATE TABLE CREDITCARD (ID INTEGER PRIMARY KEY AUTOINCREMENT,TYPE TEXT, NUMBER INTEGER, VALIDITY TEXT, USER REFERENCES USER(USERNAME));


CREATE TABLE STATION (ID INTEGER PRIMARY KEY AUTOINCREMENT,NAME TEXT);
CREATE TABLE TIMETABLE (ID INTEGER PRIMARY KEY AUTOINCREMENT,NAME TEXT,DURATION INTEGER);
